const mongoose = require("mongoose");

const flagSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
});

const Flag = mongoose.model("Flag", flagSchema);

module.exports = Flag;
